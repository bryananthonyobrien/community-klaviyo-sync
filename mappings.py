def prioritize_first_name(forward_map):
    """
    Create a reverse map prioritizing the first occurrence of each code.
    Args:
        forward_map (dict): Mapping of regions to codes.
    Returns:
        dict: Reverse map prioritizing the first name for each code.
    """
    reverse_map = {}
    for name, code in forward_map.items():
        # Only add to reverse_map if the code doesn't already exist
        if code not in reverse_map:
            reverse_map[code] = name.capitalize()
    return reverse_map


